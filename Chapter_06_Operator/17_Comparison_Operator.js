//=, == and ===
//= this is assignment operator we already disccussed

// = -- assignment opertor
// == -- loose comparision 
// === -- strict comparision 

//< , > ,>=,<= -- returns boolean 
console.log(3>4);
console.log(3<4);

console.log(4>=4);
console.log(4<=4);

console.log(5 == "5"); 
//loose comparision - it only check value it will not check type is same or not
//both values should be same
//it will not check type
//data type should not check
console.log(5 === "5");
//strict comparision
//both values  should be same
//data type should same
//it will check type

console.log(5 != 5);
console.log(5 !== 5);